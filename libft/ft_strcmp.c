#include "libft.h"

int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (1)
	{
		if (s1[i] != s2[i] || !s1[i])
			return (s1[i] - s2[i]);
		i++;
	}
}
